export function sortMatches(matches: Match[]) {
  return [...matches].sort((a, b) => {
    const byDate = a.date.localeCompare(b.date)
    if (byDate !== 0) {
      return byDate
    }
    return a.time.localeCompare(b.time)
  })
}


